CREATE TABLE STUDENTSURVEY(
	STUDENT_ID VARCHAR(32) not NULL,
	FULL_NAME VARCHAR(255), 
	STREET_ADD VARCHAR(255), 
	CITY VARCHAR(32),
	STATE VARCHAR(32), 
	ZIP VARCHAR(10), 
	PHONE VARCHAR(16), 
	EMAIL VARCHAR(255),
	URL VARCHAR(255),
	HEARFROM VARCHAR(64),
	RECOMMEND VARCHAR(32),
	FEEDBACK VARCHAR(255),
	LIKEMOST VARCHAR(255),
	TODAYDATE VARCHAR(32), 
	GRADMONTH VARCHAR(32), 
	GRADYEAR VARCHAR(32)
    );
CREATE SEQUENCE survey_seq
  MINVALUE 1
  MAXVALUE 9999999999
  START WITH 4
  INCREMENT BY 1;
  

